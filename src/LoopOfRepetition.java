import java.io.Console;

public class LoopOfRepetition {

	public static void main(String[] args) {
		for (int multiply = 1; multiply <=10; multiply++) {
			for (int number = 1; number <=10; number++) {
				System.out.print(multiply * number);
				System.out.print(" ");
			}
				
				System.out.println();
			
		}
	}

}
